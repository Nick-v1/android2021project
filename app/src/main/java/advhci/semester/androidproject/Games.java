package advhci.semester.androidproject;

public class Games {

    private String date_of_match;
    private String gcity;
    private String gcountry;
    private int gid;
    private String gsport_name;
    private String gsport_type;

    public Games() {}

    public String getDate_of_match() {
        return date_of_match;
    }

    public void setDate_of_match(String date_of_match) {
        this.date_of_match = date_of_match;
    }

    public String getGcity() {
        return gcity;
    }

    public void setGcity(String gcity) {
        this.gcity = gcity;
    }

    public String getGcountry() {
        return gcountry;
    }

    public void setGcountry(String gcountry) {
        this.gcountry = gcountry;
    }

    public int getGid() {
        return gid;
    }

    public void setGid(int gid) {
        this.gid = gid;
    }

    public String getGsport_name() {
        return gsport_name;
    }

    public void setGsport_name(String gsport_name) {
        this.gsport_name = gsport_name;
    }

    public String getGsport_type() {
        return gsport_type;
    }

    public void setGsport_type(String gsport_type) {
        this.gsport_type = gsport_type;
    }
}
